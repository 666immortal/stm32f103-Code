#ifndef _W25QXX_
#define _W25QXX_

/*********************************************************************************************************
*                                              包含头文件
*********************************************************************************************************/
#include "DataType.h"

/*********************************************************************************************************
*                                              宏定义
*********************************************************************************************************/
//W25Qxx系列芯片对应的ID列表   
#define W25Q80  0XEF13
#define W25Q16  0XEF14
#define W25Q32  0XEF15
#define W25Q64  0XEF16
#define W25Q128 0XEF17

/*********************************************************************************************************
*                                              枚举结构体定义
*********************************************************************************************************/

/*********************************************************************************************************
*                                              API函数声明
*********************************************************************************************************/
void InitW25Qxx(void);            //初始化W25Qxx
u16  W25QxxReadID(void);          //读取W25Qxx的ID

//读取SPI FLASH，在指定地址开始读取指定长度的数据
void W25QxxRead(u8* pBuffer, u32 ReadAddr, u16 NumByteToRead); 
//写SPI FLASH，在指定地址开始写入指定长度的数据，该函数带擦除操作 
void W25QxxWrite(u8* pBuffer, u32 WriteAddr, u16 NumByteToWrite); 

#endif
